#include "pipex.h"

int	ft_check_here_doc(char *str)
{
	if (str[0] == 'h' && str[1] == 'e' && str[2] == 'r' && str[3] == 'e'
		&& str[4] == '_' && str[5] == 'd' && str[6] == 'o' && str[7] == 'c')
	{
		return (1);
	}
	return (0);
}
